# operatory przynależności

data = [0,1,2,3,4,5] # lista

# czy -1 istnieje w liście data
print( -1 in data ) # False - in - czy istnieje (jest w)
print( 3 in data ) # True
print( "3" in ("3", "4") ) # True

# czy 10 nie istnieje w liście data
print( 10 not in data ) # True - not in - nie istnieje (nie ma)
print( 1 not in data ) # False
